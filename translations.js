// Current language
let currentLanguage = 'en';

// Function to translate the page
function translatePage(lang) {
    currentLanguage = lang;
    const langData = translations[lang];
    
    if (!langData) return;
    
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (langData[key]) {
            element.textContent = langData[key];
        }
    });
    
    // Update current language display
    const currentLangElement = document.getElementById('current-lang');
    if (currentLangElement) {
        const langCodes = { en: 'EN', pt: 'PT', es: 'ES', fr: 'FR' };
        currentLangElement.textContent = langCodes[lang] || 'EN';
    }
    
    // Update page title
    document.title = langData.pageTitle || 'Eco-Explorer - Wild Educational Gaming';
    
    // Store language preference
    localStorage.setItem('eco-explorer-language', lang);
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    const savedLanguage = localStorage.getItem('eco-explorer-language') || 'en';
    translatePage(savedLanguage);
}); 